def language_or_default(language: str | None) -> str:
    return language if language in TEXT else "zh"


def text(language: str | None, key: str, **kwargs) -> str:
    language = language_or_default(language)
    template = TEXT[language].get(key, TEXT["zh"].get(key, key))
    return template.format(**kwargs) if kwargs else template
